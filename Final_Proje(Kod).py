    def esittir(self):
        if (self.lineekran.text()!=""):
            self.sayitxt=(self.lineekran.text()).replace(",",".")
            self.sayi2=float(self.sayitxt)
            
            
            if (self.islem==1):
                self.lineekran.setText(str(self.sayi1+self.sayi2))
            if (self.islem==2):
                self.lineekran.setText(str(self.sayi1-self.sayi2))
            if (self.islem==3):
                self.lineekran.setText(str(self.sayi1*self.sayi2))
            if (self.islem==4):
                self.lineekran.setText(str(self.sayi1/self.sayi2))
            if (self.islem==5):
                self.lineekran.setText(str(math.sin(float(self.sayi2)*np.pi/180))) 
            if (self.islem==6):
                 self.lineekran.setText(str(math.cos(float(self.sayi2)*np.pi/180)))  
            if (self.islem==7):
                 self.lineekran.setText(str(math.tan(float(self.sayi2)*np.pi/180)))  
            if (self.islem==8):
                 self.lineekran.setText(str(math.atan(float(self.sayi2)*np.pi/180)))              
            self.islem=self.sayi1=self.sayi2=0
    
    
