    def singrafik(self):
        self.sayitxt=(self.lineekran.text())
        if ':' in self.sayitxt:
            degerler=self.sayitxt.split(":")
            genlik=float(degerler[0])
            frekans=float(degerler[1])
            print(degerler)
            print(genlik)
            print(frekans)
            t=np.arange(0,1,0.001)
            y=np.sin(2*np.pi*frekans*t)*genlik
            plt.plot(t,y)
            plt.show()
